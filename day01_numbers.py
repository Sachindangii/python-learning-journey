# Integer
a = 10
b = 3

# Basic Math
print(f"Add      : {a + b}")   # 13
print(f"Subtract : {a - b}")   # 7
print(f"Multiply : {a * b}")   # 30
print(f"Divide   : {a / b}")   # 3.33
print(f"Floor Div: {a // b}")  # 3
print(f"Remainder: {a % b}")   # 1
print(f"Power    : {a ** b}")  # 1000

# Float
x = 3.14
y = 2.0
print(f"\nFloat Add: {x + y}")

# Useful functions
print(f"\nAbsolute : {abs(-17)}")
print(f"Round    : {round(3.14159, 2)}")
print(f"Max      : {max(10, 20, 30)}")
print(f"Min      : {min(10, 20, 30)}")
print(f"Sum      : {sum([1, 2, 3, 4, 5])}")

# Real life example
age = 17
bca_years = 3
graduation_age = age + bca_years
print(f"\nMain abhi {age} saal ka hu")
print(f"BCA complete hone pe {graduation_age} saal ka hounga")
